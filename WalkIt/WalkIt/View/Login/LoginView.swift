import SwiftUI
import KakaoSDKUser
import NidThirdPartyLogin
import AuthenticationServices

struct LoginView: View {
    @ObservedObject var vm: LoginViewModel
    
    init(vm: LoginViewModel) { self.vm = vm }
    
    var body: some View {
        ZStack(alignment: .bottom) {
            LinearGradient(
                colors: [
                    Color(hex: "#B2F2FF"),
                    Color(hex: "#F0FCFF")
                ],
                startPoint: .top,
                endPoint: .bottom
            )
            VStack {
                Spacer(minLength: 50)
                
                Image("LoginLogo")
                    .resizable()
                    .scaledToFit()
                    .frame(width: 200, height: 200)
                
                HStack {
                    Spacer()
                    ZStack(alignment: .trailing) {
                        Image("LoginShadow")
                            .resizable()
                            .scaledToFit()
                            .frame(width: 146)
                            .offset(y: 140)
                        
                        Image("LoginCharacter")
                            .resizable()
                            .scaledToFit()
                            .frame(width: 250)
                    }
                }
                .offset(y: -30)
                
                
                Button {
                    vm.kakaoLogin()
                } label: {
                    Image("KakaoLogin")
                        .resizable()
                        .scaledToFit()
                        .frame(width: 300)
                }
                
                Button {
                    Task {
                        await vm.naverLogin()
                    }
                } label: {
                    Image("NaverLogin")
                        .resizable()
                        .scaledToFit()
                        .frame(width: 300)
                }
                
                ZStack {
                    SignInWithAppleButton(onRequest: { request in
                        request.requestedScopes = [.fullName, .email]
                    }, onCompletion: { result in
                        vm.handleAppleSignIn(result: result)
                    })
                    .frame(width: 300, height: 45)
                    
                    Button {
                    } label: {
                        Image("AppleLogin")
                            .resizable()
                            .scaledToFit()
                            .frame(width: 300)
                    }
                    .allowsHitTesting(false)
                }
                
                Spacer(minLength: 30)
            }
        }
        .animation(.easeOut, value: vm.showSignUpView)
        .ignoresSafeArea()
    }
}

#Preview {
    LoginView(vm: LoginViewModel())
}
